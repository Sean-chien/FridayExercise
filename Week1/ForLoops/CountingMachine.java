package Week1.ForLoops;
public class CountingMachine {
    public static void main(String[] args) {
        for (int i = 0; i <= 19; i++) {
            System.out.println("The number is: " + i);
            System.out.println(" ");

        }
        for (int i = 0; i <= 8; i++){
            System.out.println("The number is: " + i);
            System.out.println(" ");
        }

        for (int i = 0; i<= 25; i++){
            System.out.println("The number is: " + i);
            System.out.println(" ");
        }

    }
}
