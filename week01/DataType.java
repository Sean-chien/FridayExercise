package week01;

public class DataType {
    public static void main(String[] args) {

        //  1. Primitive-Types (Value Types)
        // ----- integer -----
        // - byte(1 byte, 8 bits)
        byte b = 127;
        // - short (2 bytes, 16 bits)
        short s = 200;
        // - int   (4 bytes, 32 bits)
        int i = 12343124;
        // - long  (4 or 8 bytes, 32 or 64 bits)
        long l = 124124L;
        // - floating point --
        // - float  (4 bytes)
        float f = 123.01f;
        // - double (8 bytes)
        double d = 123.1;
        // - boolean  (true, false)
        boolean bool = true;
        // - char (single char)
        char ch = 'A'; // single quote


        System.out.println(ch);
    }
}
